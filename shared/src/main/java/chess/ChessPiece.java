    public String toString() {
        String letter = switch (type) {
            case KING -> "K";
            case QUEEN -> "Q";
            case BISHOP -> "B";
            case KNIGHT -> "N";
            case ROOK -> "R";
            case PAWN -> "P";
            default -> null;
        };
        letter = color == ChessGame.TeamColor.BLACK ? letter.toLowerCase() : letter;
        return letter;
    }

