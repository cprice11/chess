package service;

public class Service {
//    The Service classes implement the actual functionality of the server. More specifically, the Service classes
//    implement the logic associated with the web endpoints.
//
//    A simple implementation of this is to have a separate Service class for each group of related endpoints. For
//    example, a UserService class might look like this:
//
//    public class UserService {
//        public AuthData register(UserData user) {}
//        public AuthData login(UserData user) {}
//        public void logout(UserData user) {}
//    }
//
//    Each service method receives a Request object containing all the information it needs to do its work. After
//    performing its purpose, it returns a corresponding Result object containing the output of the method. To do their
//    work, service classes need to make heavy use of the Model classes and Data Access classes described above.
//
//    ⚠ You must place your service classes in a folder named server/src/main/java/service.
}
