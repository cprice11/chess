package dataAccess;

public interface DAO {
//    For the most part, the methods on your DAO classes will be CRUD operations that:
//     * Create objects in the data store
//     * Read objects from the data store
//     * Update objects already in the data store
//     * Delete objects from the data store
//
//    In order to abstract from your services where data is actually being stored, you must create a Java interface that
//    hides all of the implementation details for accessing and retrieving data. In this phase you will create an
//    implementation of your data access interface that stores your server’s data in main memory (RAM) using standard
//    data structures (maps, sets, lists). In the next phase you will create an implementation of the data access
//    interface that uses an external SQL database.
//
//    data access classes
//
//    By using an interface you can hide, or encapsulate, how your data access works from the code that does not need to
//    be aware of those details. This creates a flexible architecture that allows you to change how things work without
//    rewriting all of your code. We see the benefits of this pattern in two ways.
//
//    You can quickly implement our services without having to implement a backing SQL database. This lets us focus on
//    the HTTP part of our server during this phase and then move over to SQL without changing any of our service code.
//    You can write data access tests against the memory implementation of the interface and then reuse those tests when
//    you create the SQL implementation.
//
//    ⚠ You must place your data access classes in a folder named server/src/main/java/dataAccess.
}
